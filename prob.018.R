  ncol=4,
  byrow=T
)

for (i in 1:3) {
  for (j in 1:i) {
    if (i>1 && a[i-1,j]==0 && a[i-1,j-1]==0) a[i,j]=0
    print(paste0(a[i,j]," - ", a[i+1,j]," : ",a[i,j]+a[i+1,j]))
    print(paste0(a[i,j]," - ", a[i+1,j+1]," : ",a[i,j]+a[i+1,j]))
  }
}

